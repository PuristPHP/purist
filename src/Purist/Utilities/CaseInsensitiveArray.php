<?php

namespace Purist\Utilities;

use ArrayAccess;

final class CaseInsensitiveArray implements ArrayAccess
{
    /**
     * @type array
     */
    private $array;

    public function __construct(array $array)
    {
        $this->array = $array;
    }
    /**
     * Whether a offset exists
     * @link http://php.net/manual/en/arrayaccess.offsetexists.php
     * @param mixed $offset <p>
     * An offset to check for.
     * </p>
     * @return boolean true on success or false on failure.
     * </p>
     * <p>
     * The return value will be casted to boolean if non-boolean was returned.
     * @since 5.0.0
     */
    public function offsetExists($offset)
    {
        return array_key_exists(
            strtolower($offset),
            array_change_key_case($this->array, CASE_LOWER)
        );
    }

    /**
     * Offset to retrieve
     * @link http://php.net/manual/en/arrayaccess.offsetget.php
     * @param mixed $offset <p>
     * The offset to retrieve.
     * </p>
     * @return mixed Can return all value types.
     * @since 5.0.0
     */
    public function offsetGet($offset)
    {
        return array_reduce(
            array_keys($this->array),
            function ($carry, $key) use ($offset) {
                return strtolower($key) === strtolower($offset)
                    ? $this->array[$key]
                    : $carry;
            },
            null
        );
    }

